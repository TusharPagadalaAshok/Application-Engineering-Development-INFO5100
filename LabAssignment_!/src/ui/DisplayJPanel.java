    private void displayProduct() {
            TxtName.setText(product.getName());
            TxtGeoData.setText(product.getGeoData());
            
//            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
////        product.setDOB(df.format(TxtDOB.getDate()));
            TxtDOB.setText(product.getDOB());
            
            TxtPhoneNo.setText(Long.toString(product.getPhoneno()));
            
            TxtFaxno.setText(Long.toString(product.getFaxno()));
            TxtEmailAddr.setText(product.getEmailaddr());
            
            TxtSSN.setText(Long.toString(product.getSSN()));
            TxtMedicalRecordNo.setText(Long.toString(product.getMedicalRecordno()));
            TxtHealthPlanBeneficiaryNo.setText(Long.toString(product.getHealthplanbeneficiaryno()));
            TxtBankAccNo.setText(Long.toString(product.getBankaccno()));
            TxtCertNo.setText(Long.toString(product.getCertno()));
            TxtLicensePlateNo.setText(Long.toString(product.getVechilelicenseplate()));
            TxtDeviceIdentifier.setText(Long.toString(product.getDeviceidentifier()));
            TxtIPAddr.setText(product.getIPaddr());
            TxtLinkedIn.setText(product.getLinkedin());
            
            Image im = Toolkit.getDefaultToolkit().createImage(product.getImage());
            im = im.getScaledInstance(500,500,Image.SCALE_SMOOTH);
            ImageIcon ii = new ImageIcon(im);
            Lbl_Image.setIcon(ii);
            
            
            
            
            
    }



