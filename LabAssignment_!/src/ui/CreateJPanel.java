    private void TxtNameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtNameKeyReleased
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if(Character.isLetter(c) || Character.isISOControl(c) || Character.isWhitespace(c)){
            TxtName.setEditable(true);
//            Lbl_NameWarning.setText("");
        Lbl_NameWarning.setText("Hey "+TxtName.getText()+"!");
           }
        else if(Character.isDigit(c))
                {
            evt.consume();
            Lbl_NameWarning.setText("Invalid Name!");        
            TxtName.setText(null) ;
            
//            TxtName.setEditable(false);

        }
        
    }//GEN-LAST:event_TxtNameKeyReleased

    private void TxtEmailAddrKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtEmailAddrKeyReleased
        // TODO add your handling code here:
        
        String email = TxtEmailAddr.getText().toLowerCase();
            
          if(!email.contains("@")){
              Lbl_EmailAddrWarning.setText("Incorrect Email ID Ex:test@mail.com");
//          TxtEmailAddr.setText(null);
          }
          else if(!email.contains(".com"))
            {
              Lbl_EmailAddrWarning.setText("Incorrect DOMAIN ID. Should end with .com");
             }
          else
          {
              Lbl_EmailAddrWarning.setText("I'm assuming that it's correct!");
          }
        
        
    }//GEN-LAST:event_TxtEmailAddrKeyReleased

