    private void Btn_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_SaveActionPerformed
        // TODO add your handling code here:
     
         
         //name
        product.setName(TxtName.getText());
        
        //geo data
        product.setGeoData(Option_GeoData.getSelectedItem().toString());
        
        //date
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        Date currentDate = new Date();
//        Date InvalidDate = new Date("1870-01-01");
        if(TxtDOB.getDate().after(currentDate)){
//            Lbl_DOBWarning.setText("This field is mandatory");
            Lbl_DOBWarning.setText("Your Birthday cannot be in the future!");
            JOptionPane.showMessageDialog(this,"Please correct the date."); 
            
//            TxtDOB.setText(null);
            
        }
//        else if(TxtDOB.getDate().after(InvalidDate)){
//            Lbl_DOBWarning.setText("Your age cannot be more than 150 years!");
//            JOptionPane.showMessageDialog(this,"Please correct the date.");
//            
//        }
        else{
            product.setDOB(df.format(TxtDOB.getDate()));
        }
        
        //phone number
        product.setPhoneno(Long.parseLong(TxtPhoneNo.getText())); 
         //fax number       
        product.setFaxno(Long.parseLong(TxtFaxno.getText())); 
         //email
        String email = TxtEmailAddr.getText().toLowerCase();
        if(!email.contains("@") || !email.contains(".com")){
            Lbl_EmailAddrWarning.setText("This field is mandatory");
            JOptionPane.showMessageDialog(this,"Invalid Email ID. Will be cleared! Re-Enter and Save Again!"); 
            
            TxtEmailAddr.setText(null);
        }else{
        product.setEmailaddr(TxtEmailAddr.getText());
        }
        
        //ssn
        product.setSSN(Long.parseLong(TxtSSN.getText()));
        //medical record number
        product.setMedicalRecordno(Long.parseLong(TxtMedicalRecordNo.getText()));
        //HPB number
        product.setHealthplanbeneficiaryno(Long.parseLong(TxtHealthPlanBeneficiaryNo.getText()));
        //bank acc number
        product.setBankaccno(Long.parseLong(TxtBankAccNo.getText()));
        //cert number
        product.setCertno(Long.parseLong(TxtCertNo.getText()));
        //license plate number
        product.setVechilelicenseplate(Long.parseLong(TxtLicensePlateNo.getText()));
        //device identifier
        product.setDeviceidentifier(Long.parseLong(TxtDeviceIdentifier.getText()));
        //linkedin addr
         String email_linkedin = TxtLinkedIn.getText().toLowerCase();
        if(!email_linkedin.contains("linkedin.com/")){
            Lbl_LinkedInAddrWarning.setText("This field is mandatory");
            JOptionPane.showMessageDialog(this,"Invalid LinkedIn ID. Will be cleared! Re-Enter and Save Again!"); 
            
            TxtLinkedIn.setText(null);
        }else{
            product.setLinkedin(TxtLinkedIn.getText());
        }
        //IP address
        String t = TxtIPAddr.getText().toLowerCase();
        String[] arr_ip = t.split("\\.");
                    int r=0;
                    if (arr_ip.length == 4){
                        for(int i=0;i<4;i++){
                            r = Integer.parseInt(arr_ip[i]);
                            if(r > 255){
                                Lbl_IPAddrWarning.setText("IP Address is Invalid!");
                                JOptionPane.showMessageDialog(this,"Correct the IP Address"); 
                                TxtIPAddr.setText(null);
                                
                                break;
                            }
                        }
                    }
                    else if(arr_ip.length != 4) {
                                Lbl_IPAddrWarning.setText("IP Address is not of right length");
                                JOptionPane.showMessageDialog(this,"Correct the IP Address"); 
                                TxtIPAddr.setText(null);
                                }
                    Lbl_IPAddrWarning.setText("");
                    product.setIPaddr(TxtIPAddr.getText());
                    
          //display confirmation message
          JOptionPane.showMessageDialog(this,"Correct Information is now Saved!");    
            
    }//GEN-LAST:event_Btn_SaveActionPerformed

