    public void populateCart(Menu OrderItem){
        DefaultTableModel model = (DefaultTableModel) CartTable.getModel();
        model.setRowCount(0);
        
         items.add(OrderItem);
         Object[] row = new Object[3];
                for(Menu item: items){
                     row[0] = item;
                     row[1] = item.getIngredients();
                     row[2] = item.getPrice();
                     sum = sum+Integer.parseInt(item.getPrice());
                     model.addRow(row);
                }  
     }
    
